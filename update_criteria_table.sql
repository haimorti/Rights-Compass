-- הוספת עמודה חדשה לטבלה קריטריונים_לזכאות בשם האם נדרשת נכות קבועה
ALTER TABLE קריטריונים_לזכאות
ADD COLUMN "האם נדרשת נכות קבועה" TEXT DEFAULT 'לא';

-- עדכון עמודות קיימות כדי להתאים לדרישות החדשות (דוגמה)
-- כאן ניתן לעדכן רשומות מסוימות אם יש צורך בערך 'כן'
UPDATE קריטריונים_לזכאות
SET "האם נדרשת נכות קבועה" = 'כן'
WHERE "סוג פגיעה" = 'פוסט טראומה';


